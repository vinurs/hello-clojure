(ns hello-clojure.main
  (:gen-class))
;; 文件名用下划线，命名空间用横线

;; 最基本的运行方式，读取-计算-打印-循环
;; 基本类型
;; 整数
1
42
-14

;; 小数
12.43

;; 分数
1/3
;; 分数表示法只能针对整数，对小数就会出错
;; 4.0/2
;; 有公约数的分数会自行处理
4/2

;; 有小数的除法，结果会是小数
(/ 1 3.0)
(/ 3.0 7)

;; 整数相除，除不尽的话会用分数来表示，除得尽就是整数
(/ 7 3)
(/ 2 4)
(/ 6 3)
(/ 6 4)



